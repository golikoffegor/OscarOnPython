        self.l11_sht = l11_sht
        self.podb_B15 = podb_B15
        self.sil_PPU = sil_PPU
        self.sil_IZOP = sil_IZOP

        #Переменные для учета сборных ж/б конструкций

        self.l6_l = l6_l
        self.l6_dl = l6_dl
        self.l6_pl = l6_pl
        self.l6_dpl = l6_dpl
        self.l11_l = l11_l
        self.l11_dl = l11_dl
        self.l11_pl = l11_pl
        self.l11_dpl = l11_dpl
        self.fbs336 = fbs336
        self.fbs636 = fbs636
        self.fbs936 = fbs936
        self.fbs1236 = fbs1236
        self.fbs2436 = fbs2436

        #Запас по строительны материалам

